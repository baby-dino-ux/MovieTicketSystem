CREATE PROCEDURE seed_all_open()
BEGIN
    DECLARE done INT DEFAULT 0;
    DECLARE v_id INT;
    DECLARE cur CURSOR FOR SELECT id FROM showings WHERE status='open';
    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done=1;
    OPEN cur;
    lp: LOOP
        FETCH cur INTO v_id;
        IF done THEN LEAVE lp; END IF;
        IF (SELECT COUNT(*) FROM seats WHERE showing_id=v_id)=0 THEN
            CALL seed_seats(v_id);
        END IF;
    END LOOP;
    CLOSE cur;
END$$
